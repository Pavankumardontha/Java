public class ComputeArea
{
  public static void main(String[] args)
  {
    //program to compute area of a circle
    double radius;
    double area;
    //assign radius
    radius = 20;
    //compute area
    area = radius*radius*3.14159;
    //display results
    System.out.println("The area for the circle of radius" + radius + "is" + area);
  }
}
